package Modelo;

/**
 *
 * @author LAURA
 */
public class Correo_Usuario {
    String correo, nombre, codigo;

    public Correo_Usuario(String correo, String nombre, String codigo) {
        this.correo = correo;
        this.nombre = nombre;
        this.codigo = codigo;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getCodigo() {
        return codigo;
    }

    public void setCodigo(String codigo) {
        this.codigo = codigo;
    }
    
    
}
